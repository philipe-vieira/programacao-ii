package Questao6;

public abstract class Membro {
    private String nome, dtNasc;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDtNasc() {
        return dtNasc;
    }
    public void setDtNasc(String dtNasc) {
        this.dtNasc = dtNasc;
    }
}
